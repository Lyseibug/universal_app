import 'package:flutter/material.dart';

/// Utility class to map dynamic string icons from ERPNext/Frappe into Flutter [IconData].
class IconHelper {
  /// Resolves an icon name from ERPNext to a Flutter [IconData].
  ///
  /// Maps a dynamic string name (e.g., 'inbox', 'checklist_outlined', 'receiving')
  /// to its corresponding Flutter [IconData], with a [fallback] if not found or empty.
  static IconData getIcon(String? iconName, {IconData fallback = Icons.widgets_outlined}) {
    if (iconName == null || iconName.trim().isEmpty) {
      return fallback;
    }

    // Normalize the string
    String key = iconName.trim().toLowerCase();
    
    // Remove common prefixes
    if (key.startsWith('icons.')) {
      key = key.substring(6);
    }
    if (key.startsWith('fa fa-')) {
      key = key.substring(6);
    }
    if (key.startsWith('fa-')) {
      key = key.substring(3);
    }
    if (key.startsWith('octicon-')) {
      key = key.substring(8);
    }
    
    // Replace hyphens/spaces with underscores
    key = key.replaceAll('-', '_').replaceAll(' ', '_');

    // Remove common suffixes to match base names
    // e.g., "checklist_outlined" -> "checklist"
    final cleanKey = key
        .replaceAll('_outlined', '')
        .replaceAll('_sharp', '')
        .replaceAll('_rounded', '');

    // Look up in mapped dictionary (including base and specific forms)
    final icon = _iconMap[key] ?? _iconMap[cleanKey] ?? _semanticMap[cleanKey];
    return icon ?? fallback;
  }

